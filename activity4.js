//Comparison or Relational Operator

// Define variables
let firstFavNumb = 10;  // Replace with your favorite number
let secondFavNumb = 5;  // Replace with your second favorite number

// Perform comparison operations
console.log("Greater than:", firstFavNumb > secondFavNumb);// 10 > 5
console.log("Less than:", firstFavNumb < secondFavNumb);// 10 < 5
console.log("Greater than or equal:", firstFavNumb >= secondFavNumb);// 10 >= 5
console.log("Less than or equal:", firstFavNumb <= secondFavNumb);// 10 <= 5
console.log("Strict equality:", firstFavNumb === secondFavNumb);// 10 === 5
console.log("Loose equality:", firstFavNumb == secondFavNumb);//    10 == 5
console.log("Strict inequality:", firstFavNumb !== secondFavNumb);// 10 !== 5
console.log("Loose inequality:", firstFavNumb != secondFavNumb);//    10 != 5
// Logical Operator